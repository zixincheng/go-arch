#import "SettingsTableViewController.h"

#define DEVICE_NAME @"deviceName"

@interface SettingsTableViewController ()

@end

@implementation SettingsTableViewController

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    
    self.coinsorter = [[Coinsorter alloc] init];
    
    NSUserDefaults *defaults = [NSUserDefaults standardUserDefaults];
    
    self.deviceName = [defaults objectForKey:DEVICE_NAME];
    
    [self.txtDeviceName setText:self.deviceName];
    
    // set all the text field delegates
    // this is so the return button dismisses keyboard
    self.txtDeviceName.delegate = self;
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (BOOL) textFieldShouldReturn:(UITextField *)textField {
    [textField resignFirstResponder];
    
    return YES;
}

- (IBAction)buttonPressed:(id)sender {
    [self saveSettings];
}

- (void) saveSettings {
    // save all the settings
    
    NSUserDefaults *defaults = [NSUserDefaults standardUserDefaults];
    
    [defaults setObject:self.txtDeviceName.text forKey:DEVICE_NAME];
    
    [defaults synchronize];
    
    [self.coinsorter updateDevice:self.txtDeviceName.text];
}

